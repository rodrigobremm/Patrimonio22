package com.example.iossenac.patrimonio;

public class Patrimonio {
    private int id;
    private int sala;
    private int cadeiras;
    private int mesas;
    private int projetores;
    private int totalsala;
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public int getSala() {
        return sala;
    }
    public void setSala(int sala) {
        this.sala = sala;
    }
    public int getCadeiras() {
        return cadeiras;
    }
    public void setCadeiras(int cadeiras) {
        this.cadeiras = cadeiras;
    }
    public int getMesas() {
        return mesas;
    }
    public void setMesas(int mesas) {
        this.mesas = mesas;
    }
    public int getProjetores() {
        return projetores;
    }
    public void setProjetores(int projetores) {
        this.projetores = projetores;
    }
    public int getTotalSala() {
        return totalsala;
    }

}